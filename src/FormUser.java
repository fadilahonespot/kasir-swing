      private DefaultTableModel setHeadTampil() {
        DefaultTableModel data = new DefaultTableModel();
        data.addColumn("Username");
        data.addColumn("Nama");
        data.addColumn("Email");
        data.addColumn("Telp");
        data.addColumn("Alamat");
        data.addColumn("Password");
        

        tableData.setModel(data);
        tableData.removeColumn(tableData.getColumnModel().getColumn(5));

        return data;
    }

    private void tampil() {
        DefaultTableModel data = setHeadTampil();
        try {
            Statement statement = (Statement) Koneksi.getConnection().createStatement();
            ResultSet res = statement.executeQuery("select * from users WHERE deleted_at is NULL AND tingkatan_id = 2");
            while (res.next()) {
                data.addRow(new Object[]{
                    res.getString("username"),
                    res.getString("nama"),
                    res.getString("email"),
                    res.getString("telp"),
                    res.getString("alamat"),
                    res.getString("pass")
                });

                tableData.setModel(data);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(rootPane, "Gagal Mengambil Data");
        }
    }
    
     public static String hashPassword(String passwordToHash) {
        String generatedPassword = null;

        try {
            // Create MessageDigest instance for MD5
            MessageDigest md = MessageDigest.getInstance("MD5");

            // Add password bytes to digest
            md.update(passwordToHash.getBytes());

            // Get the hash's bytes
            byte[] bytes = md.digest();

            // This bytes[] has bytes in decimal format. Convert it to hexadecimal format
            StringBuilder sb = new StringBuilder();
            for (int i = 0; i < bytes.length; i++) {
                sb.append(Integer.toString((bytes[i] & 0xff) + 0x100, 16).substring(1));
            }

            // Get complete hashed password in hex format
            generatedPassword = sb.toString();
        } catch (NoSuchAlgorithmException e) {
             JOptionPane.showMessageDialog(null, "Kesalahan hash password");
        }
        
        return generatedPassword;
    }

