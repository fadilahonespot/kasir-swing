    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       String username = tUsername.getText();
        String nama = tNama.getText();
        String email = tEmail.getText();
        String telp = tTelp.getText();
        String alamat = tAlamat.getText();

        if (username.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Data Tidak Ada");
            return;
        }

        try {
            try (java.sql.Statement statement = (java.sql.Statement) Koneksi.getConnection().createStatement()) {
                statement.executeUpdate("update users SET nama='" + nama + "'," + "email='" + email + "'," + "telp='" + telp + "'," + "alamat='" + alamat + "'" + "WHERE username = '" + username + "' AND deleted_at is null");
            }

            JOptionPane.showMessageDialog(null, "Data Berhasil di Ubah");
            tampil();
            setEmptyColumn();

        } catch (HeadlessException | SQLException t) {
            JOptionPane.showMessageDialog(null, "Data Gagal di Ubahi");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
           String username = tUsername.getText();

        if (username.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Data Tidak Ada");
            return;
        }

        try {
            LocalDateTime timeNow = java.time.LocalDateTime.now();
            Statement statement = (Statement) Koneksi.getConnection().createStatement();
            statement.executeUpdate("update users set deleted_at = '" + timeNow + "' where username = '" + username + "';");
            JOptionPane.showMessageDialog(null, "Data Berhasil di Hapus");

        } catch (HeadlessException | SQLException t) {
            JOptionPane.showMessageDialog(null, "Data Gagal di Hapus");
        }

        setEmptyColumn();
        tampil();
    }//GEN-LAST:event_jButton2ActionPerformed

