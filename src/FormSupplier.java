    private void tableDataMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableDataMouseClicked
        int i = tableData.getSelectedRow();
        if (i == -1) {
            return;
        }

        String kodeBarang = (String) tableData.getValueAt(i, 0);
        tKodeSupplier.setText(kodeBarang);
        String nama = (String) tableData.getValueAt(i, 1);
        tNama.setText(nama);
        String email = (String) tableData.getValueAt(i, 2);
        tEmail.setText(email);
        String telp = (String) tableData.getValueAt(i, 3);
        tTelp.setText(telp);
        String alamat = (String) tableData.getValueAt(i, 4);
        tAlamat.setText(alamat);
    }//GEN-LAST:event_tableDataMouseClicked

    private void bHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bHapusActionPerformed
        String kodeSupplier = tKodeSupplier.getText();

        if (kodeSupplier.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Data Tidak Ada");
            return;
        }

        try {
            LocalDateTime timeNow = java.time.LocalDateTime.now();
            Statement statement = (Statement) Koneksi.getConnection().createStatement();
            statement.executeUpdate("update supplier set deleted_at = '" + timeNow + "' where kode = '" + kodeSupplier + "';");
            JOptionPane.showMessageDialog(null, "Data Berhasil di Hapus");

        } catch (HeadlessException | SQLException t) {
            JOptionPane.showMessageDialog(null, "Data Gagal di Hapus");
        }

        setEmptyColumn();
        tampil();
    }//GEN-LAST:event_bHapusActionPerformed

