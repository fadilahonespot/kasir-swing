    private void bCariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bCariActionPerformed
        DefaultTableModel data = setHeadTampil();

        try {
            Statement statement = (Statement) Koneksi.getConnection().createStatement();
            ResultSet res = statement.executeQuery("select * from supplier where (kode like '%" + tCari.getText() + "%' OR nama like '%" + tCari.getText() + "%') AND deleted_at is null");
            while (res.next()) {
                data.addRow(new Object[]{
                    res.getString("kode"),
                    res.getString("nama"),
                    res.getString("email"),
                    res.getString("telp"),
                    res.getString("alamat")
                });

                tableData.setModel(data);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(rootPane, "Gagal Mengambil Data");
        }
    }//GEN-LAST:event_bCariActionPerformed

    private void bUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bUbahActionPerformed
        String kodeSupplier = tKodeSupplier.getText();
        String nama = tNama.getText();
        String email = tEmail.getText();
        String telp = tTelp.getText();
        String alamat = tAlamat.getText();

        if (kodeSupplier.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Data Tidak Ada");
            return;
        }

        try {
            try (java.sql.Statement statement = (java.sql.Statement) Koneksi.getConnection().createStatement()) {
                statement.executeUpdate("update supplier SET nama='" + nama + "'," + "email='" + email + "'," + "telp='" + telp + "'," + "alamat='" + alamat + "'" + "WHERE kode = '" + kodeSupplier + "' AND deleted_at is null");
            }

            JOptionPane.showMessageDialog(null, "Data Berhasil di Ubah");
            tampil();
           setEmptyColumn();

        } catch (HeadlessException | SQLException t) {
            JOptionPane.showMessageDialog(null, "Data Gagal di Ubahi");
        }
    }//GEN-LAST:event_bUbahActionPerformed

