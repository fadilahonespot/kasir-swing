    private void setComboBoxSupplier() {
        try {
            com.mysql.jdbc.Statement statement = (com.mysql.jdbc.Statement) Koneksi.getConnection().createStatement();
            ResultSet res = statement.executeQuery("select * from supplier WHERE deleted_at is null");
            while (res.next()) {
                cSupplier.addItem(res.getString("kode"));
                cSupplierFilter.addItem(res.getString("kode"));
            }
            
            cSupplier.setSelectedIndex(0);
            cSupplierFilter.setSelectedIndex(-1);
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(rootPane, "Gagal Mengambil Data");
        }
    }

    private DefaultTableModel setHeadTampil() {
        DefaultTableModel data = new DefaultTableModel();
        data.addColumn("Kode Barang");
        data.addColumn("Nama");
        data.addColumn("Harga");
        data.addColumn("Supplier");
        data.addColumn("Stok");

        tableData.setModel(data);
        return data;
    }

    private void emptyColumn() {
        tKodeBarang.setText("");
        tNamaBarang.setText("");
        tHarga.setText("");
        tStok.setText("");
    }

    private void tampil() {
        DefaultTableModel data = setHeadTampil();

        try {
            Statement statement = (Statement) Koneksi.getConnection().createStatement();
            ResultSet res = statement.executeQuery("select * from barang where deleted_at is null");
            while (res.next()) {
                data.addRow(new Object[]{
                    res.getString("kode"),
                    res.getString("nama"),
                    res.getString("harga"),
                    res.getString("kode_supplier"),
                    res.getString("stok")
                });

                tableData.setModel(data);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(rootPane, "Gagal Mengambil Data");
        }
    }

