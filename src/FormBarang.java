    private void tableDataMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableDataMouseClicked
        int i = tableData.getSelectedRow();
        if (i == -1) {
            return;
        }

        String kodeBarang = (String) tableData.getValueAt(i, 0);
        tKodeBarang.setText(kodeBarang);
        String nama = (String) tableData.getValueAt(i, 1);
        tNamaBarang.setText(nama);
        String harga = (String) tableData.getValueAt(i, 2);
        tHarga.setText(harga);
        String supplier = (String) tableData.getValueAt(i, 3);
        cSupplier.setSelectedItem(supplier);
        String stok = (String) tableData.getValueAt(i, 4);
        tStok.setText(stok);
    }//GEN-LAST:event_tableDataMouseClicked

    private void bUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bUbahActionPerformed
        String kodeBarang = tKodeBarang.getText();
        String namaBarang = tNamaBarang.getText();
        String harga = tHarga.getText();
        String supplier = cSupplier.getSelectedItem().toString();
        String stok = tStok.getText();

        if (kodeBarang.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Data Tidak Ada");
            return;
        }

        try {
            try (java.sql.Statement statement = (java.sql.Statement) Koneksi.getConnection().createStatement()) {
                statement.executeUpdate("update barang SET nama='" + namaBarang + "'," + "harga='" + harga + "'," + "kode_supplier='" + supplier + "'," + "stok='" + stok + "'" + "WHERE kode = '" + kodeBarang + "' AND deleted_at is null");
            }

            JOptionPane.showMessageDialog(null, "Data Berhasil di Ubah");
            tampil();
            emptyColumn();

        } catch (HeadlessException | SQLException t) {
            JOptionPane.showMessageDialog(null, "Data Gagal di Ubahi");
        }
    }//GEN-LAST:event_bUbahActionPerformed

