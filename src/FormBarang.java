    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        String supplier = cSupplierFilter.getSelectedItem().toString();
        String stok = tStokFilter.getText();

        String supplierQuery = "";
        if (!supplier.equals("Semua")) {
            supplierQuery = "AND kode_supplier = '" + supplier + "' ";
        }

        String stokQuery = "";
        if (!stok.isEmpty()) {
            int number = 0; 
            
            try {
                number = Integer.parseInt(stok);
            } catch (NumberFormatException ex) {
                JOptionPane.showMessageDialog(rootPane, "Input harus angka");
            }
            
            stokQuery = "AND stok <  " + number ;
        }

        DefaultTableModel data = setHeadTampil();
        try {
            Statement statement = (Statement) Koneksi.getConnection().createStatement();
            ResultSet res = statement.executeQuery("SELECT * FROM barang where deleted_at is null " + supplierQuery + stokQuery);
            while (res.next()) {
                data.addRow(new Object[]{
                    res.getString("kode"),
                    res.getString("nama"),
                    res.getString("harga"),
                    res.getString("kode_supplier"),
                    res.getString("stok")
                });

                tableData.setModel(data);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(rootPane, "Gagal Mengambil Data");
        }

    }//GEN-LAST:event_jButton5ActionPerformed


